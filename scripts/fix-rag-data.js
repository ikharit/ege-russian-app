const fixes = [];

function fixFile(filePath, fixFn) {
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let changed = 0;
  
  for (const [word, explanation] of Object.entries(data)) {
    const newExp = fixFn(word, explanation);
    if (newExp !== explanation) {
      fixes.push({ file: path.basename(filePath), word, was: explanation, now: newExp });
      data[word] = newExp;
      changed++;
    }
  }
  
  if (changed > 0) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
    console.log(`✅ Fixed ${changed} entries in ${path.basename(filePath)}`);
  }
  return changed;
}

