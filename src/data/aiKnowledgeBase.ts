/**
 * Получить знания по номеру задания.
 * Поддерживает разные форматы: "task5", "5", "task_5".
 */
export function getTaskKnowledge(taskNumber: string | number): AITaskKnowledge | undefined {
  const normalized = String(taskNumber).toLowerCase().replace(/[_\s]/g, '');
  // Try direct lookup
  if (aiKnowledgeBase[normalized]) return aiKnowledgeBase[normalized];
  // Try with "task" prefix
  if (aiKnowledgeBase[`task${normalized}`]) return aiKnowledgeBase[`task${normalized}`];
  // Try without "task" prefix
  if (normalized.startsWith('task')) {
    const num = normalized.slice(4);
    if (aiKnowledgeBase[num]) return aiKnowledgeBase[num];
  }
  return undefined;
}

/**
 * Список всех доступных заданий в базе знаний.
 */
export function getAvailableTaskNumbers(): string[] {
  return Object.keys(aiKnowledgeBase)
    .filter(k => k !== 'essay')
    .map(k => k.replace('task', ''));
}

/**
 * Случайное задание из базы знаний.
 */
export function getRandomTaskKnowledge(): { key: string; data: AITaskKnowledge } {
  const keys = Object.keys(aiKnowledgeBase);
  const key = keys[Math.floor(Math.random() * keys.length)];
  return { key, data: aiKnowledgeBase[key] };
}
