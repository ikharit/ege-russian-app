// Собираем словарь из вопросов dooshin (правильные ответы)
const DOOSHIN_WORDS: string[] = []

// Собираем слова из accentWords
const ACCENT_WORDS: string[] = []

// Все слова вместе
const ALL_WORDS = new Set([...FREQUENT_WORDS, ...DOOSHIN_WORDS, ...ACCENT_WORDS])

export const spellDictionary: Set<string> = ALL_WORDS

export function isInDictionary(word: string): boolean {
  const normalized = word.toLowerCase().replace(/[^а-яё]/gi, '')
  return spellDictionary.has(normalized) || spellDictionary.has(word.toLowerCase())
}

export function getWordInfo(word: string): { isException: boolean; rule?: string; note?: string } | null {
  const lower = word.toLowerCase()
  if (exceptionWords[lower]) {
    return { isException: true, rule: exceptionWords[lower].rule, note: exceptionWords[lower].note }
  }
  if (isInDictionary(word)) {
    return { isException: false }
  }
  return null
}

// Добавить слова в словарь (например, при импорте из dooshin)
export function addWordsToDictionary(words: string[]): void {
  for (const w of words) {
    spellDictionary.add(w.toLowerCase())
  }
}
