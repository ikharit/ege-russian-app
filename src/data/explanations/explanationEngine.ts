// Extract word from question text
// Example: "Впишите пропущенную букву: бл_стательный" → "блистательный"
export function extractWord(text: string, correctAnswer: string[]): string | null {
  const match = text.match(/([а-яёА-ЯЁ]+_[а-яёА-ЯЁ]+)/i)
  if (!match) return null

  const wordWithUnderscore = match[1]
  if (correctAnswer.length > 0) {
    return wordWithUnderscore.replace('_', correctAnswer[0].toLowerCase())
  }
  return null
}

// Detect prefix from word
export function detectPrefix(word: string): { prefix: string; description: string } | null {
  for (const { pattern, prefix, description } of prefixRules) {
    if (pattern.test(word)) {
      return { prefix, description }
    }
  }
  return null
}

// Generate explanation for task10 (prefixes)
export function generateTask10Explanation(word: string): string {
  const prefixInfo = detectPrefix(word)
  if (prefixInfo) {
    return `Приставка ${prefixInfo.prefix} ${prefixInfo.description}`
  }
  return `Проверьте правописание приставки в слове «${word}».`
}

// Generate explanation for task9 (roots)
export function generateTask9Explanation(word: string): string {
  const lowerWord = word.toLowerCase()
  if (wordExplanations[lowerWord]) {
    return wordExplanations[lowerWord]
  }
  return `Проверяемый/непроверяемый/чередующийся корень. Подберите однокоренное слово для проверки: «${word}».`
}

