// Main generate function
export function generateExplanation(
  text: string,
  correctAnswer: string[],
  atoms?: string[]
): string {
  const word = extractWord(text, correctAnswer)
  if (!word) {
    return 'Проверьте правописание.'
  }

  // Check if we have a specific explanation for this word
  const lowerWord = word.toLowerCase()
  if (wordExplanations[lowerWord]) {
    return wordExplanations[lowerWord]
  }

  // Route to task-specific generator
  if (atoms?.includes('task10')) {
    return generateTask10Explanation(word)
  }
  if (atoms?.includes('task9')) {
    return generateTask9Explanation(word)
  }
  if (atoms?.includes('task11')) {
    return generateTask11Explanation(word)
  }
  if (atoms?.includes('task12')) {
    return generateTask12Explanation(word)
  }

  return `Проверьте правописание: «${word}».`
}

// Batch process all questions in a file
export function batchGenerateExplanations(questions: any[]): any[] {
  return questions.map(q => ({
    ...q,
    explanation: generateExplanation(q.text, q.correctAnswer, q.atoms)
  }))
}

