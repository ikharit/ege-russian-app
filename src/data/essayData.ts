export const getEssayProgress = (): Record<string, EssayProgress> => {
  try {
    const raw = localStorage.getItem('essay_progress')
    if (!raw) return {}
    return JSON.parse(raw) as Record<string, EssayProgress>
  } catch {
    return {}
  }
}

export const saveEssayProgress = (progress: Record<string, EssayProgress>) => {
  localStorage.setItem('essay_progress', JSON.stringify(progress))
}

export const getEssayProgressForTopic = (topicId: string): EssayProgress | null => {
  return getEssayProgress()[topicId] || null
}

export const saveEssayProgressForTopic = (topicId: string, data: EssayProgress) => {
  const all = getEssayProgress()
  all[topicId] = data
  saveEssayProgress(all)
}

export const getEssayStats = () => {
  const all = getEssayProgress()
  const entries = Object.values(all)
  return {
    total: essayTopics.length,
    completed: entries.filter(e => e.status === 'completed').length,
    drafts: entries.filter(e => e.status === 'draft').length,
    notStarted: essayTopics.length - entries.length,
  }
}
