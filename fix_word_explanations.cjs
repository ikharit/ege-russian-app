const fs = require('fs');

const wordPath = 'src/data/explanations/wordExplanations.json';
const wordExplanations = JSON.parse(fs.readFileSync(wordPath, 'utf-8'));

const fixes = {
  'поджёг': 'Корень -жёг- (проверяемый). Проверьте через однокоренное: жечь.',
  'отобразить': 'Корень -браз- (проверяемый). Проверьте через однокоренное: образ.',
  'лёг': 'Корень -лёг- (проверяемый). Проверьте через однокоренное: ложить.',
  'запевала': 'Корень -пев- (проверяемый). Проверьте через однокоренное: петь.',
  'винегрет': 'Корень -винегрет- (непроверяемый, иноязычный). Запомните написание.',
  'вноватый': 'Корень -нов- (проверяемый). Проверьте через однокоренное: новый.',
  'рассмешить': 'Корень -смеш- (проверяемый). Проверьте через однокоренное: смех.',
  'озираться': 'Корень -зир- (проверяемый). Проверьте через однокоренное: зреть.',
  'оснащение': 'Корень -снащ- (проверяемый). Проверьте через однокоренное: снасть.',
  'исказить': 'Корень -каз- (проверяемый). Проверьте через однокоренное: сказать.',
  'напоминать': 'Корень -помин- (проверяемый). Проверьте через однокоренное: поминать.',
  'запевать': 'Корень -пев- (проверяемый). Проверьте через однокоренное: петь.',
  'проклинать': 'Корень -клин- (проверяемый). Проверьте через однокоренное: клясть.',
};

for (const [word, explanation] of Object.entries(fixes)) {
  if (wordExplanations[word]) {
    wordExplanations[word] = explanation;
    console.log('Fixed:', word);
  } else {
    console.log('Added:', word);
    wordExplanations[word] = explanation;
  }
}

fs.writeFileSync(wordPath, JSON.stringify(wordExplanations, null, 2), 'utf-8');
console.log('Saved wordExplanations.json');
